      "    perl -0pi -e 's/(<activity)/$ENV{NZ_ADMOB_PROV}\\n        $1/' \"$MANIFEST\"",
      "  fi",
      "fi",
      "",
      "echo \"Pinned AGP=$AGP, Gradle=$GRADLE.\"",
      ""
